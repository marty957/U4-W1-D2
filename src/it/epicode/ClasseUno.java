package it.epicode;

public class ClasseUno {

public void stampa(int a){
    switch (a){
        case 1:
            System.out.println("uno");
            break;
        case 2:
            System.out.println("due");
            break;
        case 3:
            System.out.println("tre");
            break;
        default:
            System.out.println("numero non valido");
            break;
    }

    }

}
